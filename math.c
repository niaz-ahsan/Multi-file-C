int addition(int a, int b) {
	return a + b;
}

int subtraction(int a, int b) {
	return a - b;
}

long long multiplication(int a, int b) {
	return (long long)a * b; // typecasting to avoid data loss
}

float division(int a, int b) {
	return (float) a / b; // typecasting to avoid data loss
}


